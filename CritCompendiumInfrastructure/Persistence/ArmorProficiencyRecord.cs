namespace CritCompendiumInfrastructure.Persistence
{
   /// <summary>
   /// Class used to store armor proficiency information.
   /// </summary>
   public sealed class ArmorProficiencyRecord
   {
      /// <summary>
      /// Gets or sets light armor proficiency.
      /// </summary>
      public bool LightArmorProficiency { get; set; }

      /// <summary>
      /// Gets or sets medium armor proficiency.
      /// </summary>
      public bool MediumArmorProficiency { get; set; }

      /// <summary>
      /// Gets or sets heavy armor proficiency.
      /// </summary>
      public bool HeavyArmorProficiency { get; set; }

      /// <summary>
      /// Gets or sets shields proficiency.
      /// </summary>
      public bool ShieldsProficiency { get; set; }

      /// <summary>
      /// Gets or sets padded proficiency.
      /// </summary>
      public bool PaddedProficiency { get; set; }

      /// <summary>
      /// Gets or sets leather proficiency.
      /// </summary>
      public bool LeatherProficiency { get; set; }

      /// <summary>
      /// Gets or sets studded leather proficiency.
      /// </summary>
      public bool StuddedLeatherProficiency { get; set; }

      /// <summary>
      /// Gets or sets hide proficiency.
      /// </summary>
      public bool HideProficiency { get; set; }

      /// <summary>
      /// Gets or sets chain shirt proficiency.
      /// </summary>
      public bool ChainShirtProficiency { get; set; }

      /// <summary>
      /// Gets or sets scale mail proficiency.
      /// </summary>
      public bool ScaleMailProficiency { get; set; }

      /// <summary>
      /// Gets or sets breastplate proficiency.
      /// </summary>
      public bool BreastplateProficiency { get; set; }

      /// <summary>
      /// Gets or sets half plate proficiency.
      /// </summary>
      public bool HalfPlateProficiency { get; set; }

      /// <summary>
      /// Gets or sets ring mail proficiency.
      /// </summary>
      public bool RingMailProficiency { get; set; }

      /// <summary>
      /// Gets or sets chain mail proficiency.
      /// </summary>
      public bool ChainMailProficiency { get; set; }

      /// <summary>
      /// Gets or sets splint proficiency.
      /// </summary>
      public bool SplintProficiency { get; set; }

      /// <summary>
      /// Gets or sets plate proficiency.
      /// </summary>
      public bool PlateProficiency { get; set; }

      /// <summary>
      /// Gets or sets shield proficiency.
      /// </summary>
      public bool ShieldProficiency { get; set; }
   }
}
